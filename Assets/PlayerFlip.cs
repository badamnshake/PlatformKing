    // OnStateUpdate is called before OnStateUpdate is called on any state inside this state machine
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        _moveX = animator.GetFloat(PlayerAnimParams.MoveX);
        if (_moveX == 0) return;
        // controlling the transform here makes sure the projectiles and other things follow direction of player
        // rather than flipping the sprite which doesn't help much
        animator.transform.localScale = _moveX > 0 ? new Vector3(1, 1, 1) : new Vector3(-1, 1, 1);
    }

